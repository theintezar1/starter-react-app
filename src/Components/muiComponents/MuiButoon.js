import * as React from 'react';
import Button from '@mui/material/Button';
import ButtonGroup from '@mui/material/ButtonGroup';
import ArrowDropDownIcon from '@mui/icons-material/ArrowDropDown';
import ClickAwayListener from '@mui/material/ClickAwayListener';
import Grow from '@mui/material/Grow';
import Paper from '@mui/material/Paper';
import Popper from '@mui/material/Popper';
import MenuItem from '@mui/material/MenuItem';
import MenuList from '@mui/material/MenuList';
import { SecondaryColor, textColor } from '../../Color.Config';

// const options = ["Self",'Son', 'Wife', 'Daughter'];

export default function MuiButton(props) {
  const {setSelectedIndex, selectedIndex} = props;
  const [open, setOpen] = React.useState(false);
  const anchorRef = React.useRef(null);
//   const [selectedIndex, setSelectedIndex] = React.useState(0);

  //get data from local storage
  let name = localStorage.getItem("name");
  let id = localStorage.getItem("userId");
  const dataArray = JSON.parse(name);
  const options = [...new Set(dataArray)];

  console.log("opetion value",selectedIndex)

  const handleClick = () => {
    console.info(`You clicked ${options[selectedIndex]}`);
  };

  const handleMenuItemClick = (event, index) => {
    setSelectedIndex(index);
    setOpen(false);
  };

  const handleToggle = () => {
    setOpen((prevOpen) => !prevOpen);
  };

  const handleClose = (event) => {
    if (anchorRef.current && anchorRef.current.contains(event.target)) {
      return;
    }

    setOpen(false);
  };

  return (
    <React.Fragment>
      <ButtonGroup bgcolor={SecondaryColor} ref={anchorRef} aria-label="split button">
        <Button sx={{height:"30px", backgroundColor:SecondaryColor, color:textColor, fontFamily: 'Josefin Sans, sans-serif',textTransform:"none",}} onClick={handleClick}>{options[selectedIndex]}</Button>
        <Button
          sx={{height:"30px", bgcolor:SecondaryColor, color:textColor}}
          size="small"
          aria-controls={open ? 'split-button-menu' : undefined}
          aria-expanded={open ? 'true' : undefined}
          aria-label="select merge strategy"
          aria-haspopup="menu"
          onClick={handleToggle}
        >
          <ArrowDropDownIcon />
        </Button>
      </ButtonGroup>
      <Popper
        sx={{
          zIndex: 1,
        }}
        open={open}
        anchorEl={anchorRef.current}
        role={undefined}
        transition
        disablePortal
      >
        {({ TransitionProps, placement }) => (
          <Grow
            {...TransitionProps}
            style={{
              transformOrigin:
                placement === 'bottom' ? 'center top' : 'center bottom',
            }}
          >
            <Paper>
              <ClickAwayListener onClickAway={handleClose}>
                <MenuList id="split-button-menu" autoFocusItem>
                  {options.map((option, index) => (
                    <MenuItem
                      key={option}
                      selected={index === selectedIndex}
                      onClick={(event) => handleMenuItemClick(event, index)}
                    >
                      {option}
                    </MenuItem>
                  ))}
                </MenuList>
              </ClickAwayListener>
            </Paper>
          </Grow>
        )}
      </Popper>
    </React.Fragment>
  );
}